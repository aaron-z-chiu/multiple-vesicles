close all; clear; clc;


data1 = load('energy1.m');
data2 = load('energy2.m');

E1 = data1(:, 3);
E2 = data2(:, 3);

dt = 2e-5;
nPoints = length(E1);
t = (0:nPoints-1) * dt;

figure;

plot(t, E1, 'b-', ...
    'Marker', 'o', ...
    'MarkerFaceColor', 'b', ...
    'MarkerEdgeColor', 'b', ...
    'LineWidth', 1.2, ...
    'MarkerIndices', 1:1000:length(E1));
hold on;

plot(t, E2, 'r-', ...
    'Marker', '^', ...
    'MarkerFaceColor', 'r', ...
    'MarkerEdgeColor', 'r', ...
    'LineWidth', 1.2, ...
    'MarkerIndices', 1:1000:length(E2));

hold off;

xlabel('Time (s)');
ylabel('Energy');
grid on;

legend("Vesicle 1","Vesicle 2");

set(gca, 'FontSize', 14);

text('Interpreter','latex', ...
     'String','$x$', ...
     'Position',[0.891962081682177 -0.0577590895778421 0], ...
     'FontSize',19);
text('Interpreter','latex', ...
     'String','$y$', ...
     'Position',[-0.0749639883567335 0.915105165952411 0], ...
     'FontSize',19);


tt = sprintf('energy.eps');
print('-deps', tt);
